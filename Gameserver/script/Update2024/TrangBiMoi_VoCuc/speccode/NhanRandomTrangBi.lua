local tbOpt = {
{"<color=yellow>Nhận Trang Bị", self.NhanTrangBi_Kim, self},
};
Dialog:Say(szMsg, tbOpt);
end

function tbSystem:NhanTrangBi_Kim()

if (me.nSex == 0) then --Nam
local tbItems = MathRandom(1,#self.Nam_Kim)
local pItem = me.AddItem(unpack(self.Nam_Kim[tbItems]));
pItem.Bind(1);
end 

if (me.nSex == 1) then --Nữ 
local tbItems = MathRandom(1,#self.Nu_Kim)
local pItem = me.AddItem(unpack(self.Nu_Kim[tbItems]));
pItem.Bind(1);
end 

end

